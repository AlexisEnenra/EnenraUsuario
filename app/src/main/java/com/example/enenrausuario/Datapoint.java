package com.example.enenrausuario;

public class Datapoint {
    int xValue , yValue;

    public Datapoint(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public Datapoint(){

    }

    public int getxValue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }
}
